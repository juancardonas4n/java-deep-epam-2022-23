package org.epamcourse.sorter;

import java.util.Arrays;




/**
 * Sorter class groups methods for sorting comparable array objects
 *  @author Andrés Felipe Sánchez Sánchez
 * */
public class Sorter {


    /**
     * This methods sort in standard way the array with Arrays.sort
     *
     * @param ar: array of comparable objects to be sorted
     *@see Arrays#sort(int[])
     *
     * */

    public static  void sort(Object[] ar){

        Arrays.sort(ar);

    }

}
